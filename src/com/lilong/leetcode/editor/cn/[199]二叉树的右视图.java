import java.util.ArrayList;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {
    public List<Integer> rightSideView(TreeNode root) {
        List<Integer> list = new ArrayList<>();
        if (root == null) {
            return list;
        }
        int depth=0,max=-1;
        rightShowView(root, list,depth,max);
        return list;
    }

    public void rightShowView(TreeNode root, List<Integer> list,int depth,int max) {
        if (root == null) {
            return;
        }
        if(depth>max){
            list.add(root.val);
            max=depth;
        }
        depth++;
        rightShowView(root.right, list,depth,max);
        rightShowView(root.left,list,depth,max);
        depth--;
    }
}
